// Program to use a switch statement

void main() {
  String input = 'apple'; // Change this value to test different cases
  
  switch (input) {
    case 'apple':
      print('You chose an apple.');
      break;
    case 'banana':
      print('You chose a banana.');
      break;
    case 'orange':
      print('You chose an orange.');
      break;
    default:
      print('Unknown fruit.');
  }
}

